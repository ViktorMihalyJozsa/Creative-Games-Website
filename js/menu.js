// Az elso oldal betoltese a : 'greeting.html' tartalommal...
document.addEventListener('DOMContentLoaded', function () {  // Az oldal betoltese utan...
    firstContent('pages/greeting.html');                     // Az oldal tartalmanak a betoltese...
});

    // Az oldal tartalmanak betoltese...
    function firstContent(url) {  // Az oldal tartalmanak URL-je...
        fetch(url)                // Az oldal tartalmanak URL-je...
            // A valasz ellenorzese...
            .then(response => {
                if (!response.ok) {                                                    // Ha a valasz nem OK...
                    throw new Error('<h4>Error code : ' + response.status + '</h4>');  // Hiba uzenet...
                }
                // A valasz szoveg atalakitasa...
                return response.text();
            })
            // Az oldal tartalmanak beallitasa...
            .then(data => {                                                // A valasz tartalmanak beallitasa...
                document.getElementById('firstContent').innerHTML = data;  // Az oldal tartalmanak beallitasa...
            })
            // A hiba uzenet beallitasa...
            .catch(error => {                                                                                          // Hiba eseten...
                document.getElementById('firstContent').innerHTML = '<h2>ERROR</h2><h4>' + error.message + ' !</h4>';  // Hiba uzenet...
            })
    }


