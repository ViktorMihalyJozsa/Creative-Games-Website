    function openNav() {
        document.getElementById('menuContent').style.width = "100%";     // A menu megnyitasa...
        document.getElementById('menuContent').style.height = "100%";    // A menu megnyitasa...
        document.getElementById('menuContent').style.transition = '1s';  // A menu megnyitasa animacioval...
    }

    function closeNav() {
        document.getElementById('menuContent').style.width = "100%";     // A menu bezarasa...
        document.getElementById('menuContent').style.height = "0%";      // A menu bezarasa...
        document.getElementById('menuContent').style.transition = '1s';  // A menu bezarasa animacioval...
    }

