function loadContent(menuContent) { 
 
    document.getElementById("menuContent").style.height = "0%";      // A menu bezarasa...
    document.getElementById('menuContent').style.transition = '1s';  // A menu bezarasa animacioval...

    fetch(menuContent)  // Az oldal tartalmanak URL-je...
        // A valasz ellenorzese...
        .then(response => {
            if (!response.ok) {                                                    // Ha a valasz nem OK...
                throw new Error('<h4>Error code : ' + response.status + '</h4>');  // Hiba uzenet...
            }
            // A valasz szoveg atalakitasa...
            return response.text();
        })
        // A valasz tartalmanak beallitasa...
        .then(data => {                                                // A valasz tartalmanak beallitasa...
            document.getElementById('firstContent').innerHTML = data;  // A tartalom beállítása...
        })
        // Hiba eseten...
        .catch(error => {
            document.getElementById('firstContent').innerHTML = '<h2>ERROR</h2><h4>' + error.message + ' !</h4>';  // Hiba uzenet...
        })
}